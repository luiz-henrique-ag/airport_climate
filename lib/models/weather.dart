class Weather {
  String atmosphericPressure;
  String visibility;
  double wind;
  double moisture;
  String condition;
  double temperature;
  String conditionDesc;

  Weather({
    required this.atmosphericPressure,
    required this.visibility,
    required this.wind,
    required this.moisture,
    required this.condition,
    required this.temperature,
    required this.conditionDesc
  });
}